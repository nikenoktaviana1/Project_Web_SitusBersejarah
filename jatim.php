          <p style="text-align: justify;">&nbsp; &nbsp; &nbsp;Dalam perjalanan sejarah bangsa, proses pembentukan struktur pemerintahan dan wilayah Jawa Timur ternyata memiliki perjalanan sangat panjang. Dari sumber-sumber epigrafis dalam bentuk batu bertulis (Prasasti Dinoyo) diketahui bahwa sejak abad VIII, tepatntya tahun 760 di Jawa Timur telah muncul suatu satuan pemerintahan, Kerajaan Kanjuruhan di Malang, dengan status yang sampai kini masih diperdebatkan.</p>

          <p style="text-align: justify;" >&nbsp; &nbsp; &nbsp;Pada abad X, Jawa Timur menapaki fase baru. Jawa Timur yang semula merupakan wilayah pinggiran dari Kerajaan Mataram Kuno di Jawa Tengah, kemudian mendapatklan momentum sebagai pusat kekuasaan berbagai kerajaan, seperti Medang (937 - 1017), Daha-Janggala (1080-1222), Singasari (1222-1292) dan Majapahit (1293 -1527). Dalam hal ini , Pu Sendok (927-947) adalah tokoh paling berjasa yang berhasil meletakkan dasar-dasar pemerintahan di Jawa Timur. Struktur pemerintahannya secara hierarkhis terdiri dari Pemerintah Pusat (Kraton), Watek (Daerah) dan Wanua (Desa). Struktur ini terus bertahan sampai abad XIII zaman Singasari.</p>

          <p style="text-align: justify;" >&nbsp; &nbsp; &nbsp;Pada abad XIII terjadi perkembangan baru dalam struktur ketatanegaraan di Indonesia di Jawa Timur, ditandai dengan munculnya sebuah struktur baru dalam pemerintahan, yaitu Nagara (Provinsi). Berdasarkan Prasasti Mulamalurung (1255) dari masa Wisnu Wardhana yang juga bergelar Sminingrat menyatakan bahwa struktur pemerintahan Singasari dari Pusat (Kraton), Nagara (Provinsi), Watek (Kabupaten) dan Wanua (Desa).</p>
        
          <p style="text-align: justify;" >&nbsp; &nbsp; &nbsp;Pada masa Kerajaan Majapahit, susunan itu mendapatkan berbagai penyempurnaan, terdiri dari Bhumi (Pusat/Kraton), Negara (Provinsi/Bhatara), Watek/Wisaya (Kabupaten/Tumenggung), Lurah/Kuwu (Kademangan), Thani/Wanua (Desa/Petinggi) dan paling bawah Kabuyutan (Dusun/Rama). Anehnya struktur kenegaraan Majapahit (1294-1527) justru berkembang secara ketat pada masa Mataram (1582 -1755). Wilayah Mataram dibagi secara konsentris terdiri dari Kuthagara/Nagara (Pusat/Kraton), Negaragung/Negaraagung (Provinsi Dalam), Mancanegara (Provinsi luar ), Kabupaten dan Desa. Secara etimologis, sebutan Jawa Timur pada zaman Mataram Islam muncul derngan nama Bang Wetan, derngan wilayah meliputi seluruh Pesisir Wetan dan Mancanagara Wetan (pedalaman Jawa Timur).</p>

          <p style="text-align: justify;" >Selanjutnya setelah huru-hara Cina di Kartasura (1743), seluruh wilayah pesisir utara Jawa dan seluruh Pulau Madura jatuh ke tangan Kompeni, sedang daerah Mataram tinggal wilayah pedalaman Jawa (Mancanagara Wetan -Mancanagara Kulon). Dengan berakhirnya Perang Dipanegara (1830), seluruh Jawa Timur (BangWetan) dapat dikuasai Pemerintah Hindia Belanda. Dari tahun 1830-1928 /1929, Belanda menjalankan pemerintahan dengan hubungan langsung Pemerintah Pusat VOC di Batavia derngan para Bupati yang berada di wilayah kekuasaanya. Pemerintah Hindia Belanda yang sejak awal abad XX menerapkan politik imperialisme modern melakukan intensifikasi pemerintahan dengan membentuk Pemerintahan Provinsi Jawa Timur (Provincient van Oost Java ) pada tahun 1929, dengan struktur pemerintahan, wilayah dan birokrasi tidak jauh berbeda seperti yang ada sekarang. Pada masa pendudukan Jepang (1942-1945) seperti daerah lain, Jawa Timur diletakkan dibawah pendudukan militer Jepang.</p>

          <p style="text-align: justify;" > &nbsp; &nbsp; &nbsp;Setelah Proklamasi Kemerdekaan, Pemerintah Republik Indonesia mulai menata kehidupan kenegaraan. Berdasarkan Pasal 18 Undang-undang Dasar 1945 pada tanggal 19 Agustus 1945 olehPPKI dibentuklah Provinsi dan penentuan para Gubernurnya. Untuk Pertama kalinya, R.M.T. Soeryo yang kala itu menjabat Residen Bojonegoro ditunjuk sebagai Gubernur Jawa Timur yang pertama. R.M.T Soeryo yang dilantik tanggal 5 September 1945, sampai tanggal 11 Oktober 1945 harus menyelesaikan tugas-tugasnya di Bojonegoro, dan baru pada 12 Oktober 1945 boyong ke Surabaya, Ibukota Provinsi Jawa Timur yang menandai mulai berputarnya mekanisme Pemerintahan Provinsi Jawa Timur. Atas dasar pertimbangan perjalanan sejarah inilah, maka diterbitkan Peraturan Daerah Provinsi Jawa Timur Nomor 6 Tahun 2007 tentang Hari Jadi Provinsi Jawa Timur yang menetapkan tanggal 12 Oktober sebagai Hari Jadi Provinsi Jawa Timur yang menetapkan tanggal 12 Oktober sebagai Hari Jadi Jawa Timur dan akan diperingati secara resmi setiap tahun, baik ditingkat Provinsi maupun Kabupaten/Kota di seluruh Jawa Timur.

          <br> Sumber : Buku Peringatan Hari Jadi Ke -70 Provinsi Jawa Timur</p>

